package ch06.example;

public class Food { //클래스선언
	//필드 선언
	String foodName;
	int count;
	String expDate;
	String etc;
	//생성자 선언
	public Food () {}
	
	
	public Food(String foodName, int count, String expDate, String etc) {
		this.foodName = foodName;
		this.count = count;
		this.expDate = expDate;
		this.etc = etc;
	} 
	
	
	
}
